package org.mifos.dmt.logging;

import java.io.IOException;
import java.io.PrintWriter;
import java.util.ArrayList;

import javax.servlet.ServletConfig;
import javax.servlet.ServletException;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

public class LogStream extends HttpServlet {
	private static final long serialVersionUID = -5606503198186287778L;

	public void init(ServletConfig config) throws ServletException {
		super.init(config);
	}

	protected void doGet(HttpServletRequest request,
			HttpServletResponse response) throws ServletException, IOException {
		response.setContentType("text/html");
		PrintWriter out = response.getWriter();
		DMTLogStore logstore = DMTLogStore.getInstance();
		ArrayList<ArrayList<String>> logsArray= logstore.getLogQueue();
		for(ArrayList<String> log : logsArray) {
			String openSpan = "<span class=\""+log.get(0)+"\">";
			String closeSpan = "<br></span>";
			out.println(openSpan +log.get(0).toUpperCase() + " - " + log.get(1)+closeSpan);
			
		}

	}
}
